// 數字常見的用法 (整數 浮點數)

System.Console.WriteLine(9 / 2);
System.Console.WriteLine(9 + 2);
System.Console.WriteLine(9 - 2);
System.Console.WriteLine(9 * 2);

// 如想把計算結果顯示為點數，可將整數寫法改為浮點數
System.Console.WriteLine(9.0 / 2);

// 可設定變數
int num = 20;
System.Console.WriteLine(num * 2);

// 絕對值 System.Math.Abs
System.Console.WriteLine(System.Math.Abs(-12));

// 次方 System.Math.Pow
System.Console.WriteLine(System.Math.Pow(2,4));

// 根號 System.Math.Sqrt
System.Console.WriteLine(System.Math.Sqrt(81));

// 比較較大的數字 System.Math.Max
System.Console.WriteLine(System.Math.Max(81,100));

// 比較較大的數字 System.Math.Min
System.Console.WriteLine(System.Math.Min(81, 100));

// 四捨五入 System.Math.Round
System.Console.WriteLine(System.Math.Round(77.2));